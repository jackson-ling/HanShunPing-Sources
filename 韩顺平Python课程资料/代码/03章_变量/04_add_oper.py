# +号的使用案例

name = "king"
score = 50.8
num1 = 10
str1 = " ok"

print(score + num1)  # 60.8
print(name + str1)  # king ok
print(score + 90)  # 140.8
print(name + " hi")  # king hi
print("100" + "98")  # 10098
print(34.5 + 100)  # 134.5

